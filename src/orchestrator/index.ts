function getOrchestrator(
  authProvider: AuthProvider,
  orchestratorUrl?: string,
  headers?: Record<string, string>,
): Orchestrator {
  return new Orchestrator(
    orchestratorUrl ?? PROD_ORCHESTRATOR_URL,
    authProvider,
    headers,
  )
}

